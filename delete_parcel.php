 header('Content-Type: application/json');


require_once 'DB_Functions.php';
$db = new DB_Functions();
 
// json response array
$response = array("error" => FALSE);
 
if (isset($_POST['otp'])){
 
    // receiving the post params
    $otp =isset($_POST['otp']) ? $_POST['otp'] : '';
    
 
    $otp=test_input($otp);

     
   
        $user = $db->ride_delete_parcel($otp);

